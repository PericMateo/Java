    public void crtaj(int brKvadrata, int x, int y){
    
        int vis = 0;
        for(int i=0;i<brKvadrata;i++){
            vis+=15;
            Graphics2D ploca = (Graphics2D)jPanel1.getGraphics();
            ploca.drawRect(x-vis, y-vis, 30+vis*2, 30+vis*2);
        }
    }
    public void CrtajIme(String ime){
    
        Graphics2D ploca = (Graphics2D)jPanel1.getGraphics();
        Font myFont = new Font("Arial",Font.BOLD,24);
        ploca.setColor(Color.red);
        ploca.setFont(myFont);
        ploca.drawString(ime, 24, 24);
        
    }
    public String obrniIme(String ime){
    
        
        StringBuilder ime1 = new StringBuilder();
        ime1.append(ime);
        ime1.reverse();
        String obrnutoIme = ime1.toString();
        return obrnutoIme;
    }

